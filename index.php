        function toggleLoading(){
          $('#wrapper-loading').toggleClass('active');
        }
        
        function toggleLoadingOff(){
          $('.modal-backdrop').toggleClass('show');
        }

         function messageSuccess(){
          $("#alertas").modal('show');
        }

         function messageErro(){
          $("#erro_message").modal('show');
        }


        $('#ajaxRequest').submit(async function(e){
          e.preventDefault();
          let formSerialize = $(this).serializeArray();
          formSerialize = await formSerialize.reduce( (reduce,item) => {
            reduce[item.name] = item.value;
            return reduce;
          },{})
            toggleLoading()
          $.ajax({
            url: "./services/action.php",
            data:formSerialize,
            success:function(data){
              toggleLoading();
              const response = JSON.parse(data);
              if(response.status === 200){
                toggleLoadingOff();
                messageSuccess();
                window.location.href = response.data.paymentUrl;
              }
            },
            error:function(data){
              if(response.status === 200){
                window.location.href = response.data.paymentUrl;
              }
            },
            beforeSend:function(data){
              console.log('Send request...')
            }
          })
        });

      </script>
